public class DataTypeDemo
{
	public static void main(String[] args)
	{
		int number = 1; // 4 Byte
		if (number >= 1)
		{
			System.out.println("True");
		}

		boolean flag = true; // 1 bit 

		if (flag)
		{
			System.out.println("True");
		}

		long length = 1; // 8 Byte

		if (length >= 1)
		{
			System.out.println("True");
		}
	}

	// 10^6
	// 128MB
	// 4GB
	// 8GB
}